    ID          NUMBER(20) NOT NULL,
    NODEID      NUMBER(20),
    LABEL_NAME  VARCHAR2(50 CHAR),
    LABEL_VALUE VARCHAR2(64 CHAR),
    REMARK      VARCHAR2(255 CHAR) DEFAULT '' ,
    IS_DEL      NUMBER(3) DEFAULT 0 NOT NULL,
    TENANT_ID   VARCHAR2(64 CHAR) DEFAULT '',
    CREATE_USER VARCHAR2(32 CHAR) DEFAULT '',
    CREATE_TIME DATE DEFAULT SYSDATE NOT NULL,
    UPDATE_USER VARCHAR2(32 CHAR) DEFAULT '',
    UPDATE_TIME DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT PK_T_BPMN_NODE_LABELS PRIMARY KEY (ID)
);


CREATE INDEX INDX_NODE_ID ON T_BPMN_NODE_LABELS (NODEID);

CREATE SEQUENCE SEQ_T_BPMN_NODE_LABELS START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

CREATE
OR REPLACE TRIGGER TRG_T_BPMN_NODE_LABELS_ID
BEFORE INSERT ON T_BPMN_NODE_LABELS
FOR EACH ROW
WHEN (NEW.ID IS NULL)
BEGIN
SELECT SEQ_T_BPMN_NODE_LABELS.NEXTVAL
INTO :NEW.ID
FROM DUAL;
END;
/





